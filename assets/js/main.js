// ── Countdown Timer ──
function initCountdown(targetDate, containerId) {
  const container = document.getElementById(containerId);
  if (!container) return;

  function update() {
    const now = new Date().getTime();
    const target = new Date(targetDate).getTime();
    const diff = target - now;

    if (diff <= 0) {
      container.innerHTML = '<p style="font-size:1.25rem;font-weight:700;color:var(--maroon);">Event Telah Dimulai! 🎉</p>';
      return;
    }

    const days = Math.floor(diff / (1000 * 60 * 60 * 24));
    const hours = Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((diff % (1000 * 60)) / 1000);

    container.querySelector('[data-days]').textContent = String(days).padStart(2, '0');
    container.querySelector('[data-hours]').textContent = String(hours).padStart(2, '0');
    container.querySelector('[data-minutes]').textContent = String(minutes).padStart(2, '0');
    container.querySelector('[data-seconds]').textContent = String(seconds).padStart(2, '0');
  }

  update();
  setInterval(update, 1000);
}


