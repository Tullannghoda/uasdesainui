// ── Smooth scroll for anchor links ──
function initSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
      const targetId = this.getAttribute('href');
      if (targetId === '#') return;
      const target = document.querySelector(targetId);
      if (target) {
        e.preventDefault();
        const offset = parseInt(getComputedStyle(document.documentElement).getPropertyValue('--navbar-height')) || 72;
        const top = target.getBoundingClientRect().top + window.pageYOffset - offset;
        window.scrollTo({ top, behavior: 'smooth' });
      }
    });
  });
}


// ── Set Active Nav Link & ScrollSpy ──
function setActiveNavLink() {
  const currentPage = window.location.pathname.split('/').pop() || 'index.html';
  const navLinks = document.querySelectorAll('.nav-link');
  
  // Set active for exact page match
  navLinks.forEach(link => {
    const href = link.getAttribute('href');
    if (href === currentPage || (currentPage === '' && href === 'index.html')) {
      link.classList.add('active');
    }
  });

  // ScrollSpy logic for hash links (e.g. #tentang, #event)
  if (currentPage === 'index.html' || currentPage === '') {
    const spySections = ['beranda', 'tentang', 'event'];
    
    window.addEventListener('scroll', () => {
      let current = '';
      const offset = parseInt(getComputedStyle(document.documentElement).getPropertyValue('--navbar-height')) || 72;
      
      spySections.forEach(id => {
        const section = document.getElementById(id);
        if (section) {
          // Add 50px buffer to trigger earlier when scrolling down
          const sectionTop = section.offsetTop - offset - 50; 
          if (window.pageYOffset >= sectionTop) {
            current = id;
          }
        }
      });
      
      if (current) {
        navLinks.forEach(link => {
          const href = link.getAttribute('href');
          if (href && href.startsWith('#')) {
            link.classList.remove('active');
            if (href === `#${current}`) {
              link.classList.add('active');
            }
          }
        });
      }
    }, { passive: true });
  }
}


